void network_init() {
	wifiManager = new WiFiManager();
	
	wifiManager->setConfigPortalTimeout(90);

	client = new WiFiClient();
	ssl_client = new WiFiClientSecure();
	
	ssl_client->setCACert(ca_crt);
	ssl_client->setTimeout(8000);

	if(WiFi.begin() != WL_CONNECT_FAILED) {
	  if(WiFi.waitForConnectResult(WIFI_CONN_TIMEOUT)) {
		return;
	  }
	}

	wifiManager->autoConnect("ConnectThing");
	
	if(!WiFi.isConnected()) {
		Serial.println("network_init(): failed to connect to wifi: resetting..");
		ESP.restart();
	}
}